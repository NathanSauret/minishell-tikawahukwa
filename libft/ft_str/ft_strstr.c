#include "../libft.h"

int	ft_strstr(char *str, char *cmp, int n)
{
	int	i;

	i = 0;
	while (str[i] == cmp[i] && i < n)
		i++;
	if ((ft_is_space(str[i]) || !str[i]) && i == n)
		return (1);
	else
		return (0);
}
